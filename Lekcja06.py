# seasons
month = int(input('Enter number of month : '))
if month == 3 or month == 4 or month == 5:
    print('Spring')
elif month == 6 or month == 7 or month == 8:
    print('Summer')
elif month == 9 or month == 10 or month == 11:
    print('Autumn')
elif month == 12 or month == 1 or month == 2:
    print('Winter')
else:
    print('Incorrect month!')
